<h2>Dusseldorf - the capital of North Rhine-Westphalia </h2>

<p style="text-indent:20px">
Dusseldorf is a large city in West Germany located 
in the Rhine-Ruhr region, which is the official capital 
of the federal states of North Rhine-Westphalia and the 
seat of the government district of Dusseldorf. It is 
located in the lower reaches of the Rhine, on its right 
bank, at the confluence with the Dussel River. Some of 
the districts of Dusseldorf, such as Lerik, Oberkassel, 
Heerdt and Niederkassel are located on the left bank of 
the Rhine. In total, the city includes 10 districts, subdivided 
into 49 districts and numbering about 600,000 local residents. 
</p>

<p style="text-indent:20px">
Dusseldorf is one of the 5 largest political, economic, cultural 
and transport centers in Germany along with Hamburg, Berlin, Munich 
and Frankfurt am Main. It is the leading center for advertising, 
telecommunications technology and patent science in Europe. It 
hosts the world's largest printing exhibition "Drupa" and the 
equally popular model exhibition "Collections Premieren Dusseldorf", 
thanks to which Dusseldorf is called the German "city of fashion". </p>


